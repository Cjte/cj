                <h2>Our history</h2>
                <p>A brief history of Methodism
'Methodists' was originally a nickname applied to a revival movement in 18th
century Britain, based within the Church of England and led by, among others, the
brothers John and Charles Wesley.
Childhood home
Born into the large family of Samuel Wesley, Rector of Epworth in Lincolnshire, John
[1703-1791] and Charles [1707-1788] owed a great deal to their remarkable mother
Susanna, as well as to the Puritan background of both parents.
Oxford and America
Both brothers studied at the University of Oxford (at Christ Church) and John went
on to become a Fellow of Lincoln College, Oxford. In the early 1730â€™s, a small group
of students met regularly for Bible study and prayer, received Communion frequently
and undertook works of charity; such devout behaviour was unusual in those times
and they were soon ridiculed. Among others in the 'Holy Club' (another nickname)
was George Whitefield who would become the greatest preacher of the time. The
Wesleys, Whitefield and other leaders were mostly ordained clergy of the Church of
England. The 'Methodists' in Oxford were a short-lived group, but they set a pattern
for the 'Evangelical Revival'.
In 1735 the Wesleys responded to an invitation to serve as chaplains to American
colonies; this was unsuccessful, and both had returned to Britain by 1738. Although
neither returned to America, some 50 years later their followers (such as Francis
Asbury and Thomas Coke) did, and so Methodism spread in the 'New World'.
Aldersgate
Influenced by the Moravians the Wesleys joined in a 'Religious Society' in London,
and in May 1738 both underwent a profound spiritual experience. John famously
described this in his Journal for 24 May 1738
"In the evening I went unwillingly to a society in Aldersgate Street, where one was
reading Luther and preface to the Epistle to the Romans. About a quarter to nine,
while he was describing the change which God works in the heart through faith in
Christ, I felt my heart strangely warmed. I felt I did trust in Christ, Christ alone for
salvation, and an assurance was given me that he had taken away my sins, even
mine and saved me from the law of sin and death."
Three days earlier, following his own 'conversion', Charles had written a hymn:
Where shall my wondering soul begin
How shall I all to heaven aspire?
2
For the following half-century such hymns flowed from Charles' pen (it is estimated
he wrote over 6,000), while John was the organising genius who turned a
spontaneous movement into structured body which became the origin of today's
world-wide Methodist Church.
Preaching
In 1739 Whitefield invited John Wesley to preach to crowds of working classes in
Bristol in the open air. Since such people were often excluded from the churches,
'field preaching' became a key feature of the Revival, and Wesley recorded
addressing gatherings of many thousands. His published Sermons became and
remain the doctrinal standard of the Methodist Church.
Societies and classes
Wesley formed converts into local societies, originally modelled upon the 'Religious
Societies' and his Oxford group; they were also subdivided into 'classes' which met
weekly. Every year, by horse or carriage, John Wesley travelled the country to visit,
encourage and admonish the societies, as well as preaching. He insisted that
Methodists regularly attend their local parish church as well as Methodist meetings.
Through the societies, members supported one another spiritually and pastorally,
and working people and women often found a status otherwise denied to them.
'Social righteousness'
For the Wesleys, 'works' as well as faith were essential to the whole of Christian
living, and caring for the poor, for prisoners, for widows and orphans mattered a
great deal. Methodists were not only interested in welfare, they were concerned to
remedy social injustice, and John Wesley's last known letter urged the abolition of
'that execrable villainy'; black slavery. The Wesleys were an influence in prison
reform and, inspired by Susanna Wesley, they earned a reputation as pioneers in
education. John Wesley wrote, edited or abridged some 400 publications.
Hymns
Among Charles' hymns, still sung today, are numbered some of the finest ever
written; and through them the Methodists received and expressed their Christian
experience and learned their beliefs. His poetic genius drew upon his classical and
literary education and an awareness of popular culture, as well as his musical
talents. After his marriage in 1749, Charles remained mostly in London and Bristol.
Doctrine
The assurance of the free grace of God was the experience of the early Methodists,
which the Wesleys set in the Christian tradition of 'arminianism', emphasising within
human freewill the need for holy living as an outcome of faith leading towards
'Christian perfection'. The Calvinists (such as Whitefield) by contrast stressed the
absolute sovereignty of God and believed in predestination.
3
Separation
Although Wesley declared, "I live and die a member of the Church of England", the
strength and impact of the movement, especially after John Wesley's clandestine
ordinations in 1784, made a separate Methodist body virtually inevitable.
In the 19th century Methodism in Britain flowed in several channels, including
Primitive Methodism which began with 'camp meetings' in 1807 and was organised
into a separate body in 1811. The Methodists grew to be a large, respectable and
influential section of society; characterised by the 'nonconformist conscience' and
also the 'temperance movement' and many members with poor origins became
prosperous. The missionary movement also spread the Methodist message around
the world.
In 1932 the three main Methodist groups in Britain came together to form the present
Methodist Church.
The Methodist Conference and Connexion
As the Methodist societies grew at a fast rate, some way of keeping in touch and
organising them was needed. John Wesley had held what became an annual
conference of Methodist preachers. In 1784 he made provision for the continuance
as a corporate body after his death of the 'Yearly Conference of the People called
Methodists'. He nominated 100 people and declared them to be its members and laid
down the method by which their successors were to be appointed.
After his death the leadership passed to the Methodist Conference, and instead of
one person exercising leadership for a length of time, the President of the
Conference became for the year of office the representative of the Conference and
leading minister of the church. During the nineteenth century there were many
factions in the church. Gradually most of these were re-united, the last union being in
1932.
The Methodist Church has a Connexional structure rather than a congregational one.
This is where the whole church acts and decides together. It is where the local
church is never independent of the rest of the Connexion. Everyone who becomes a
member through confirmation is a member of the Methodist Church as a whole, not
just their local church.
The Methodist Church is part of the whole Church of Christ. It claims no superiority
or inferiority to any other part of the Church. All those who confess Jesus Christ as
Lord and Saviour and accept the obligations to serve him in the life of the Church
and the world are welcome as full members of the Methodist Church.
